			   <p>Engagement is the measure of audience responses to a brand's content and activity on a social network.</p>
			   <p>To calculate the engagement score, we weigh audience interactions on brand content such as Likes, Comments, Shares or Favorites, Replies and Retweets based on their importance. We then divide their weighted sum by our estimate of the number of brand fans and followers who actively receive and view such content. </p>
