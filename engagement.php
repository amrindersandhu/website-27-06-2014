			<h3>Rationale</h3>
			<h4>Background</h4>
			<p>At Unmetric, we derived our engagement score formulas through user research and observations on the features and functionalities of different social media platforms.</p>
			<p>Engagement scores for brands are often calculated using the total number of audience responses such as Likes, Comments, Shares or Retweets divided by the total number of fans or followers of a brand. Use of this methodology provides a weak measure of true engagement because a brand with a large number of fans/followers will be unfairly penalized for their bigger fan/follower numbers.</p>
 
			<p>We also noticed that social media managers and marketers associate varying importance to audience responses such Comments, Shares and Retweets based on the type of interaction that matters the most to them. However, engagement formulas used so far have not allowed for such flexibility.</p>
 
			<p>We set out to develop a way to calculate engagement that avoids these pitfalls.</p>
 
			<h4>Why 0.8?</h4>
			<p>We observed that because of the way social networks are set up, the greater the number of fans/followers a brand has, the smaller the percentage of its fans/followers who stand to view the brand's content. Thus, a brand with 1 million fans/followers will have its content seen by a smaller percentage of its fans/followers than a brand with 100,000.</p>
			<p>We first observed this effect viewing Facebook's internal algorithm in action. Our analysts using empirical data points found a way to estimate the number of brand fans/followers who stand to actively receive and view a brand's content. They discovered that the reception rate of a brand's Facebook post best varies as a function of the number of brand fans to the power of 0.8. When we similarly estimated the reception rate across different social networks such as Twitter, LinkedIn, and Instagram, we found that the power value consistently approximated a value of 0.8.</p>

			<h4>Weights</h4>
			<p>Usually, weights refer to the depth of an idea or how important an idea is. On social networks, weights become the strength with which a particular response -a Comment, a Share or a Retweet - influences the calculation of the resulting Engagement Score.</p>
			<p>In the default Unmetric formulae, Comments and Replies are weighed higher than Favorites and Likes because Comments and Replies start a conversation. Shares and Retweets are weighed higher than Comments and Replies because Shares and Retweets intentionally amplify audience reach of a tweet.</p>
			<p>We arrived at the optimal weight values of 5 and 10 by surveying a sample of users of the Unmetric Platform from top brands and agencies.</p>
		</section>
		<hr class="divider">
		
		<section class="content-wrap">
			<h3>Research</h3>
			<p>We came across external empirical studies that corroborate our findings and rationale behind the Unmetric engagement score formulas.</p>
			<p>In 2013, social scientists at Stanford published a study titled 'Quantifying The Invisible Audience In Social Networks', where they analyzed Facebook audience logs for 220,000 user posts. Here, they discovered that similar to Facebook fan pages, the audience reached by a user post varies as a function of the number of friends a Facebook user has. That is, the greater the number of friends a user has, the smaller the fraction of people who saw the user's posts. <a href="http://hci.stanford.edu/publications/2013/invisibleaudience/invisibleaudience.pdf">Read more</a>.</p>
			<p>In 2010, a team of scientists at Palo Alto Research Center (PARC) analyzed a dataset of 74 million tweets and built a predictive Retweet model. In a study titled, 'Want to be Retweeted? Large Scale Analytics on Factors Impacting Retweet in Twitter Network', they suggest that the greater the number of followers a user has, it more likely that their Tweet will be Retweeted. <a href="http://extweb-prod.parc.com/content/attachments/want-to-be-retweeted.pdf">Read more</a>.</p>
		</section>
		<hr class="divider">
		
		<section class="content-wrap">
			<h3>Advantages</h3>
 
			<h4>Fairness & Accuracy</h4>
			<p>The Unmetric engagement formulas compare all brands equally by accounting for differences in the number of fans/followers. In other words, all brands are measured against the same yardstick.</p>
			<p>The engagement score does not unfairly penalize brands with a large number of fans/followers. As a result, the only differences are the audience responses on the content itself such as Likes, Comments, Replies or Retweets making the Unmetric engagement score an accurate representation of the performance of brand content.</p>
 
			<h4>Normalization</h4>
			<p>Unmetric engagement scores fall in a range of 1-1000. After applying the Unmetric formula for calculation of the Engagement Score for a brand's content, we normalize the score to fall within this range.</p>
 
			<p>When we say our engagement scores are normalized, we mean that we adjust the resulting engagement values given by the formula to a common scale where the minimum value is 0 and the maximum possible value is 1000.</p>
			<p>We normalize our engagement scores because it gives users a measure to compare and gauge how well a brand and its content are doing by providing a cap on the maximum possible value.</p>

			<h5>Normalization Formula</h5>
			<div class="engmnt_formula-wrap">
				<p>Normalized Engagement Score = 1000*(1 - e<sup>(-Engagement Score/factor*)</sup>)</p>
			</div>
			<p>* factor value is 1000 or 3000 based on social network.</p>
 
			<h4>Customization</h4>
			<p>We understand that our users may prefer to use different values for weights to accord varying importance to audience responses.  Our users may also prefer to use the total number of fans/followers or just the total number of audience responses on content, based on their needs.</p>
			<p>For this reason, we provide the ability for users to customize the weights of Comments, Shares, Replies and Retweets and the Audience Reception Rate as per their requirements to calculate the engagement score within the Unmetric Platform.</p> 
		</section>
		
